#!/usr/bin/perl
$x=2;
$y=3;
$res = $x+$y;
print "Addition : $res\n";
$res = $x-$y;
print "Substraction : $res\n";

$res = $x*$y;
print "Multiplication : $res\n";
$res = $x/$y;
print "Division : $res\n";

$res = $x%$y;
print "Remainder Operation : $res\n";

$res = $x**$y;
print "Power : $res\n";